package tac.kbp.kb.index;

public class Pair {
	
	float k1;
	float b;
	
	public Pair(float k1_, float b_) {
		this.b=b_;
		this.k1=k1_;
	}
	
	public float getK1() {
		return k1;
	}
	
	public void setK1(float k1) {
		this.k1 = k1;
	}
	
	public float getB() {
		return b;
	}
	
	public void setB(float b) {
		this.b = b;
	}

}
